operation=input()
second_number=int(input())
if operation=='+':
    print(first_number+second_number)
elif operation=='-':
    print(first_number-second_number )
elif operation=='*':
    print(first_number*second_number )
elif operation=='/':
    print(first_number/second_number )
 
